# Range function

# Range function, it's a very interesting function.Actually it can get three arguments-for example: range(start,stop,step)

num = list(range(10))
print(num)
print(num[2]) # print 2 number index


num1 = list(range(1,11)) # Two argument-counter,and stop
print(num1)


num2 = list(range(2,26,2)) # Three arguments-start,stop,step
print(num2)
